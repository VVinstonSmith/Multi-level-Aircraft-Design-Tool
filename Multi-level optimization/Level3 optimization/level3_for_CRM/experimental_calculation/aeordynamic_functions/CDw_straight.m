function CD = CDw_straight(Ma, t_bar, A)
% 平直机翼波阻系数
% picture(06, 7-12)

% x1 = sqrt(1-Ma^2)
% x2 = A * t_bar^(1/3)
% CD = y * t_bar^(5/3)

x1_data = [0, 0.2, 0.4, 0.5, 0.6, 0.7, 0.8, 0.9 , 1];
x2_data = [0.5, 1, 1.5, 2, 3, 4];
y_data = [
    0.909, 0.818, 0.705, 0.636, 0.559, 0.468, 0.373, 0.295, 0.227;
    2.324, 2.199, 2.037, 1.891, 1.705, 1.414, 1.045, 0.759, 0.523;
    3.023, 2.986, 2.909, 2.823, 2.681, 2.394, 1.977, 1.477, 1;
    3.304, 3.28, 3.238, 3.187, 3.093, 2.909, 2.542, 1.886, 1.273;
    3.5, 3.467, 3.444, 3.416, 3.35, 3.234, 2.917, 2.37, 1.59;
    3.636, 3.627, 3.59, 3.568, 3.536, 3.397, 3.14, 2.685, 1.909];
%
x1 = sqrt(1-Ma^2);
x2 = A * t_bar^(1/3);
if x2 > 4
    x2 = 4;
end
y = interp2(x1_data, x2_data, y_data, x1, x2, 'Linear');
CD = y * t_bar^(5/3);

end

