function y = get_d_alpha0_d_tao(ka_q, A, lamda)
% 每度扭转角引起的零升迎角增量
ka_q_deg = ka_q * 180/pi;

% lamda_data = [0, 0.5, 1];
ka_q_deg_data = [0, 5, 10, 15, 20, 25, 30, 35, 40, 45, 50];
A_data = [1.5, 3.5, 6];

% lamda = 0
y_data_1 = [
    -0.4, -0.3993, -0.3986, -0.3979, -0.3971, -0.396, -0.395, -0.3932, -0.3911, -0.389, -0.384;
    -0.382, -0.379, -0.3768, -0.3742, -0.3715, -0.369, -0.366, -0.363, -0.36, -0.356, -0.35;
    -0.367, -0.3637, -0.3606, -0.3575, -0.354, -0.351, -0.347, -0.343, -0.339, -0.335, -0.331];
% lamda = 0.5
y_data_2 = [
    -0.4071, -0.4068, -0.4057, -0.405, -0.4043, -0.4036, -0.4025, -0.4014, -0.4, -0.3983, -0.3959;
    -0.4104, -0.4079, -0.4057, -0.4034, -0.4011, -0.3985, -0.3963, -0.3939, -0.391, -0.3882, -0.3846;
    -0.4129, -0.4109, -0.4089, -0.4064, -0.4043, -0.4018, -0.3996, -0.3974, -0.3952, -0.393, -0.3896];
% lamda = 1
y_data_3 = [
    -0.4074, -0.4081, -0.4088, -0.4092, -0.4093, -0.4091, -0.4081, -0.4068, -0.405, -0.4027, -0.3996;
    -0.4174, -0.4167, -0.4156, -0.4143, -0.4133, -0.412, -0.4107, -0.4094, -0.4082, -0.4071, -0.4061;
    -0.427, -0.4257, -0.4244, -0.4232, -0.4221, -0.4211, -0.4196, -0.4185, -0.4175, -0.4167, -0.4163;
    -0.4374, -0.435, -0.4329, -0.431, -0.429, -0.4271, -0.4256, -0.4243, -0.4236, -0.4232, -0.4233];

if A<=6
    y1 = interp2(ka_q_deg_data, A_data, y_data_1, ka_q_deg, A);
    y2 = interp2(ka_q_deg_data, A_data, y_data_2, ka_q_deg, A);
    y3 = interp2(ka_q_deg_data, [A_data,10], y_data_3, ka_q_deg, A);
else
    y1 = interp2(ka_q_deg_data, A_data, y_data_1, ka_q_deg, 6);
    y2 = interp2(ka_q_deg_data, A_data, y_data_2, ka_q_deg, 6);
    y3 = interp2(ka_q_deg_data, [A_data,10], y_data_3, ka_q_deg, A);
end
if lamda>=0 && lamda<0.5
     y = y1*(0.5-lamda) + y2*(lamda-0);
     y = y / (0.5-0);
elseif lamda>=0.5 && lamda<=1
    y = y2*(1-lamda) + y3*(lamda-0.5);
    y = y / (1-0.5);
end
end

