    1.31067961165049, 2.57281553398058, 3.55339805825243;
    1.68932038834951, 2.97087378640777, 3.91262135922330;
    2.21359223300971, 3.47572815533981, 4.43689320388349;
    2.66019417475728, 4.00970873786408, 5.05825242718447;
    3.15533980582524, 4.42718446601942, 5.41747572815534];
